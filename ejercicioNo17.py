# pedir el radio de un circulo y dar su area y logitud de la circunfgerencia
r = float(input("ingresa el radio del circulo, puede ser decimal : "))
pi = 3.1416
area = pi * r**2
longitud = 2 * pi * r
print ("----------------------------------------------------")
print (f"el radio del circulo es : {r}");
print (f"el area del circulo es : {area}");
print (f"la longitud de la circunferencia es : {longitud}");

